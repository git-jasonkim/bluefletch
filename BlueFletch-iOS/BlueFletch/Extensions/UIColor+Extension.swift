import UIKit

extension UIColor {
    static func blueFletchBlue() -> UIColor {
        return UIColor(red: 1/255, green: 95/255, blue: 169/255, alpha: 1.0)
    }
    
    static func blueFletchWhite() -> UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    static func blueFletchOffWhite() -> UIColor {
        return UIColor(white: 0.99, alpha: 1.0)
    }
    
    static func blueFletchBlack() -> UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
}
